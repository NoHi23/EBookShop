    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
//        processRequest(request, response);
        request.getRequestDispatcher("register.jsp").include(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
//        processRequest(request, response);
        try {
            String name = request.getParameter("name");
            String email = request.getParameter("email");
            String phone = request.getParameter("phone");
            String password = request.getParameter("password");
            String check = request.getParameter("check");

            User us = new User();
            us.setName(name);
            us.setEmail(email);
            us.setPhone(phone);
            us.setPassword(password);

            if (check != null) {
                UserDAO dao = new UserDAO();
                User u = dao.checkUserExist(email);
                if (u == null) {
                    dao.register(name, email, phone, password);
                    request.getSession().setAttribute("success", "Registration Successful");
                    request.getRequestDispatcher("login.jsp").include(request, response);
                } else {
                    request.getSession().setAttribute("wrong", "Email already exists");
                    request.getRequestDispatcher("register.jsp").include(request, response);
                }
            } else {
                request.getSession().setAttribute("wrong", "Please Check Agree term & Condition");
                request.getRequestDispatcher("register.jsp").include(request, response);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

